def _run_copilot(
    prompt: str,
    configuration: Configuration,
    *,
    cwd: Path | None = None,
    project_root: Path | None = None,
    run_output: Path | None = None,
    idle_timeout: int = _IDLE_TIMEOUT,
    max_retries: int = 2,
    resolved: ResolvedConfiguration | None = None,
    generation_model: str | None = None,
) -> tuple[dict, SessionTrace | None]:
    """Invoke the Copilot CLI with a watchdog that kills hung processes.

    Uses ``--output-format json`` to capture the full event stream
    directly to ``{run_output}/events.jsonl``.  This is the same data
    the Copilot CLI writes to ``~/.copilot/session-state/``, so we
    parse it in-place for session tracing — no filesystem guessing.

    Returns ``(usage_dict, session_trace)``.
    """
    cmd = ["copilot", "-p", prompt, "--yolo", "--output-format", "json"]

    if generation_model:
        cmd.extend(["--model", generation_model])

    # Use resolved plugin paths if available, otherwise fall back to legacy
    plugin_paths: list[Path] = []
    if resolved:
        plugin_paths = resolved.plugin_paths
    elif project_root:
        plugin_paths = [
            (project_root / ref.local_path).resolve()
            for ref in configuration.plugins if ref.local_path
        ]

    for plugin_path in plugin_paths:
        cmd.extend(["--plugin-dir", str(plugin_path)])

    plugin_display = [str(p) for p in plugin_paths]
    click.echo(f"  Running: copilot -p <prompt> --yolo" + (
        f" --plugin-dir {' '.join(plugin_display)}"
        if plugin_paths else ""
    ))
    if cwd:
        click.echo(f"  Working directory: {cwd}")

    # Give the Copilot CLI more memory to avoid OOM on large prompts.
    # Append to any existing NODE_OPTIONS to avoid overriding Copilot's own settings.
    existing_opts = os.environ.get("NODE_OPTIONS", "")
    if "--max-old-space-size" not in existing_opts:
        env = {**os.environ, "NODE_OPTIONS": f"{existing_opts} --max-old-space-size=8192".strip()}
    else:
        env = dict(os.environ)

    for attempt in range(1 + max_retries):
        if attempt > 0:
            click.echo(f"  ⚠️  Retry {attempt}/{max_retries}")

        # Write Copilot JSON output directly to the run's events.jsonl.
        # On retries, use a suffixed name to avoid truncating partial data.
        if run_output:
            run_output.mkdir(parents=True, exist_ok=True)
            if attempt == 0:
                events_path = run_output / "events.jsonl"
            else:
                events_path = run_output / f"events-attempt-{attempt}.jsonl"
            stdout_fh = open(events_path, "w", encoding="utf-8")
        else:
            stdout_fh = tempfile.NamedTemporaryFile(
                mode="w", suffix=".jsonl", delete=False, prefix="copilot-out-",
            )
            events_path = Path(stdout_fh.name)

        start_time = time.monotonic()
        proc = subprocess.Popen(
            cmd, cwd=cwd, stdout=stdout_fh, stderr=subprocess.PIPE, env=env,
        )
        timed_out = _watchdog_wait(proc, idle_timeout)
        elapsed = time.monotonic() - start_time
        stdout_fh.close()

        stderr_output = ""
        if proc.stderr:
            stderr_output = proc.stderr.read().decode(errors="replace").strip()

        # Parse full session trace from JSON output
        trace: SessionTrace | None = None
        try:
            trace = parse_events_file(events_path)
            if trace is not None and trace.total_events == 0:
                trace = None  # discard empty/incomplete traces
        except OSError:
            pass

        if timed_out:
            click.echo(f"  ⚠️  Copilot idle for {idle_timeout}s — killing (PID {proc.pid})")
            # Try to capture usage before killing — work may have completed
            usage = _parse_copilot_log_usage(
                proc.pid, session_id=trace.session_id if trace else None,
            )
            _kill_process_tree(proc)
            if usage:
                usage["wall_time_seconds"] = round(elapsed, 1)
                usage["timed_out"] = True
                mins, secs = divmod(int(elapsed), 60)
                click.echo(
                    f"    📊 (timed out) {usage.get('input_tokens', 0):,} in / "
                    f"{usage.get('output_tokens', 0):,} out / "
                    f"{usage.get('api_calls', 0)} calls / {mins}m {secs}s"
                )
            if attempt < max_retries:
                continue
            raise RuntimeError(
                f"Copilot CLI hung after {max_retries + 1} attempts "
                f"for configuration '{configuration.name}'",
                usage,
            )

        if proc.returncode != 0:
            if stderr_output:
                click.echo(f"    Copilot stderr: {stderr_output}")
            # Check if output was actually produced despite non-zero exit.
            # The Copilot CLI may exit non-zero for post-completion issues
            # (e.g., notification script failures) while the work completed.
            if cwd:
                output_dir = cwd / "output"
                has_output = output_dir.exists() and any(output_dir.rglob("*.md"))
            else:
                has_output = False
            if has_output:
                click.echo(
                    f"    ⚠️  Copilot exited with code {proc.returncode} "
                    f"but output files exist — treating as success"
                )
            else:
                # Retry on crash/OOM (high exit codes)
                if attempt < max_retries:
                    click.echo(f"    ⚠️  Will retry ({attempt + 1}/{max_retries})")
                    continue
                msg = (
                    f"Copilot CLI exited with code {proc.returncode} "
                    f"for configuration '{configuration.name}'"
                )
                raise RuntimeError(msg)

        # Parse usage from Copilot CLI log (use session ID for reliable matching)
        sid = trace.session_id if trace else None
        usage = _parse_copilot_log_usage(proc.pid, session_id=sid)
        if usage:
            usage["wall_time_seconds"] = round(elapsed, 1)
            mins, secs = divmod(int(elapsed), 60)
            click.echo(
                f"    📊 {usage.get('input_tokens', 0):,} in / "
                f"{usage.get('output_tokens', 0):,} out / "
                f"{usage.get('api_calls', 0)} calls / {mins}m {secs}s"
            )
        if usage is None:
            usage = {}
        if trace and trace.session_id:
            usage["session_id"] = trace.session_id
        # On successful retry, rename events file to the canonical name
        if attempt > 0 and run_output:
            canonical = run_output / "events.jsonl"
            try:
                if canonical.exists():
                    canonical.unlink()
                events_path.rename(canonical)
            except OSError:
                pass
        return usage, trace


def _watchdog_wait(proc: subprocess.Popen, idle_timeout: int) -> bool:
    """Wait for *proc* to finish, killing it if truly idle.

    Returns True if the process was killed due to idle timeout.

    A process is considered **active** if ANY of these are true:
    - CPU usage increased by ≥0.5s since last check
    - It has active TCP connections (waiting on API responses)

    Only triggers the timeout when NEITHER condition is met for
    *idle_timeout* consecutive seconds. This avoids false positives
    for I/O-bound processes waiting on large LLM API responses.
    """
    idle_since = time.monotonic()
    last_cpu = _get_process_cpu(proc.pid)
    poll_interval = 15  # seconds between checks
    min_cpu_delta = 0.5  # minimum CPU seconds per interval

    while proc.poll() is None:
        time.sleep(poll_interval)
        if proc.poll() is not None:
            break

        current_cpu = _get_process_cpu(proc.pid)
        if current_cpu is None:
            break  # process gone

        cpu_delta = current_cpu - (last_cpu or 0)
        last_cpu = current_cpu

        has_cpu = cpu_delta >= min_cpu_delta
        has_network = _has_active_connections(proc.pid)

        if has_cpu or has_network:
            idle_since = time.monotonic()
        elif time.monotonic() - idle_since > idle_timeout:
            return True  # truly idle: no CPU AND no network

    return False


def _get_process_cpu(pid: int) -> float | None:
    """Get cumulative CPU time for a process. Returns None if process not found."""
    try:
        if sys.platform == "win32":
            result = subprocess.run(
                ["powershell", "-NoProfile", "-Command",
                 f"(Get-Process -Id {pid} -ErrorAction SilentlyContinue).CPU"],
                capture_output=True, text=True, timeout=10,
            )
            val = result.stdout.strip()
            return float(val) if val else None
        else:
            stat = Path(f"/proc/{pid}/stat")
            if stat.exists():
                fields = stat.read_text().split()
                utime = int(fields[13])
                stime = int(fields[14])
                return float(utime + stime)
            return None
    except Exception:
        return None


def _has_active_connections(pid: int) -> bool:
    """Check if a process has active TCP connections (ESTABLISHED state).

    Returns True if the process is connected to a remote server,
    indicating it's waiting on an API response — not truly idle.
    """
    try:
        if sys.platform == "win32":
            result = subprocess.run(
                ["powershell", "-NoProfile", "-Command",
                 f"Get-NetTCPConnection -OwningProcess {pid} -State Established "
                 f"-ErrorAction SilentlyContinue | Measure-Object | "
                 f"Select-Object -ExpandProperty Count"],
                capture_output=True, text=True, timeout=10,
            )
            val = result.stdout.strip()
            return int(val) > 0 if val else False
        else:
            # On Linux, check /proc/net/tcp for the pid
            result = subprocess.run(
                ["sh", "-c", f"ss -tnp state established | grep -c 'pid={pid},'"],
                capture_output=True, text=True, timeout=10,
            )
            val = result.stdout.strip()
            return int(val) > 0 if val else False
    except Exception:
        return False


def _kill_process_tree(proc: subprocess.Popen) -> None:
    """Kill a process and its children."""
    if sys.platform == "win32":
        try:
            subprocess.run(
                f"taskkill /F /T /PID {proc.pid}",
                shell=True, capture_output=True, timeout=10,
            )
        except Exception:
            pass
    else:
        import signal as _signal
        try:
            os.killpg(os.getpgid(proc.pid), _signal.SIGKILL)
        except (ProcessLookupError, OSError):
            pass
    try:
        proc.wait(timeout=10)
    except Exception:
        pass


def _parse_copilot_log_usage(pid: int, session_id: str | None = None) -> dict | None:
    """Parse token usage from the Copilot CLI log file.

    Copilot CLI writes detailed logs to ~/.copilot/logs/ with per-request
    token counts. When *session_id* is provided, matches the log file
    containing that session (most reliable). Falls back to PID-based
    matching, then to the most recent log.
    """
    import re as _re

    log_dir = Path.home() / ".copilot" / "logs"
    if not log_dir.exists():
        return None

    log_file: Path | None = None
    content: str | None = None

    # Strategy 1: Find log containing the session ID (most reliable)
    if session_id:
        session_marker = f'"session_id": "{session_id}"'
        candidates = sorted(
            log_dir.glob("process-*.log"),
            key=lambda f: f.stat().st_mtime,
            reverse=True,
        )
        for f in candidates[:10]:
            try:
                file_content = f.read_text(encoding="utf-8", errors="replace")
                if session_marker in file_content:
                    log_file = f
                    content = file_content
                    break
            except OSError:
                continue
        if not log_file:
            return None

    # Strategy 2 (no session ID): Match by PID, then most recent log
    if not log_file:
        matching = list(log_dir.glob(f"process-*-{pid}.log"))
        if matching:
            log_file = matching[0]
    if not log_file:
        logs = sorted(log_dir.glob("process-*.log"), key=lambda f: f.stat().st_mtime)
        if logs:
            log_file = logs[-1]

    if not log_file:
        return None

    if content is None:
        try:
            content = log_file.read_text(encoding="utf-8", errors="replace")
        except Exception:
            return None

    # Extract token usage — if we have a session ID, only count entries
    # for this specific session to avoid cross-contamination from shared logs.
    if session_id:
        # Parse structured blocks: each "assistant_usage" telemetry entry
        # has session_id and metrics with token counts.
        blocks = content.split('"kind": "assistant_usage"')
        input_tokens = []
        output_tokens = []
        cache_read = []
        durations = []
        session_marker = f'"session_id": "{session_id}"'
        for block in blocks[1:]:  # skip first chunk (before first block)
            if session_marker not in block:
                continue
            inp = _re.findall(r'"input_tokens": (\d+)', block)
            out = _re.findall(r'"output_tokens": (\d+)', block)
            crd = _re.findall(r'"cache_read_tokens": (\d+)', block)
            dur = _re.findall(r'"duration": (\d+)', block)
            if inp:
                input_tokens.append(int(inp[0]))
            if out:
                output_tokens.append(int(out[0]))
            if crd:
                cache_read.append(int(crd[0]))
            if dur:
                durations.append(int(dur[0]))
    else:
        # Fallback: extract all token entries from the file
        input_tokens = [int(m) for m in _re.findall(r'"input_tokens": (\d+)', content)]
        output_tokens = [int(m) for m in _re.findall(r'"output_tokens": (\d+)', content)]
        cache_read = [int(m) for m in _re.findall(r'"cache_read_tokens": (\d+)', content)]
        durations = [int(m) for m in _re.findall(r'"duration": (\d+),', content)]

    if not input_tokens:
        return None

    return {
        "api_calls": len(input_tokens),
        "input_tokens": sum(input_tokens),
        "output_tokens": sum(output_tokens),
        "cache_read_tokens": sum(cache_read) if cache_read else 0,
        "api_duration_ms": sum(durations) if durations else 0,
    }


def run_generate(
    config: EvalConfig,
    project_root: Path,
    configurations: list[str] | None = None,
    resume: bool = False,
    resolver: SourceResolver | None = None,
    scenario_offset: int = 0,
) -> None:
    """Generate code for each configuration defined in the config.

    Each scenario is generated in its own Copilot invocation so that
    large skills/plugins don't exhaust the context window before later
    scenarios can be built.

    When ``config.runs > 1``, each configuration is generated N times with
    output stored under ``output/{config}/run-{N}/{scenario}/``.

    Args:
        config: The parsed evaluation configuration.
        project_root: Root directory of the evaluation project.
        configurations: Optional list of configuration names to run.
                        If None, runs all configurations.
        resume: If True, skip runs where output already exists.
        resolver: Optional source resolver for remote skill sources.
        scenario_offset: Added to run_id for round-robin scenario
                         selection.  Auto-improve passes the turn number
                         so that each iteration cycles to the next scenario.
    """
    configs_to_run = config.configurations
    if configurations:
        valid_names = {c.name for c in config.configurations}
        invalid = set(configurations) - valid_names
        if invalid:
            raise click.ClickException(
                f"Unknown configuration(s): {', '.join(sorted(invalid))}. "
                f"Valid: {', '.join(sorted(valid_names))}"
            )
        configs_to_run = [c for c in config.configurations if c.name in configurations]

    output_base = project_root / config.output.directory
    output_base.mkdir(parents=True, exist_ok=True)
    _ensure_gitignore(output_base)

    total_scenarios = len(config.scenarios)
    num_runs = config.runs
    all_usage: list[dict] = []

    # Pre-resolve all sources if resolver is available
    resolved_configs: dict[str, ResolvedConfiguration] = {}
    if resolver:
        resolved_configs = resolver.resolve_all(config)

    for cfg in configs_to_run:
        label = cfg.label or cfg.name
        resolved = resolved_configs.get(cfg.name)

        click.echo(f"\n{'=' * 60}")
        click.echo(f"Generating: {label}")
        click.echo(f"Runs:       {num_runs}")
        click.echo(f"Scenarios:  {total_scenarios} available (1 selected per run, round-robin)")
        click.echo(f"{'=' * 60}")

        # Snapshot the global skill_directories so we can restore later.
        # Then replace with ONLY the skills for this configuration to
        # prevent contamination from previous configs or manual registrations.
        saved_skill_dirs = get_skill_directories()

        # Collect all include_directories from all scenarios so they're
        # available regardless of which scenario is selected for each run.
        all_include_dirs: list[IncludeDirectory] = []
        seen_paths: set[str] = set()
        for scenario in config.scenarios:
            for inc in scenario.include_directories:
                if inc.path not in seen_paths:
                    all_include_dirs.append(inc)
                    seen_paths.add(inc.path)

        staging_dir, staging_links = _create_staging_dir(
            project_root, cfg, resolved=resolved,
            include_directories=all_include_dirs,
        )
        added_skills: list[Path] = []
        try:
            # Determine skill paths (resolved or legacy)
            skill_paths: list[Path] = []
            if resolved:
                skill_paths = resolved.skill_paths
            else:
                skill_paths = [
                    (project_root / ref.local_path).resolve()
                    for ref in cfg.skills if ref.local_path
                ]

            # Clear all global skill_directories, then add only this config's
            if skill_paths:
                skill_abs = [str(p) for p in skill_paths]
                set_skill_directories(skill_abs)
                added_skills = skill_paths
                skill_display = [ref.display_name for ref in cfg.skills]
                click.echo(
                    f"  Registered skills: "
                    f"{', '.join(skill_display)}"
                )
            else:
                set_skill_directories([])
                click.echo("  Skill directories cleared (no skills for this config)")

            for run_id in range(1, num_runs + 1):
                run_output = output_base / cfg.name / f"run-{run_id}"

                # Resume support: skip if output exists
                if resume and run_output.exists() and any(run_output.iterdir()):
                    click.echo(f"\n  ⏭️  Run {run_id}/{num_runs} — skipping (output exists)")
                    continue

                # Select one scenario for this run (round-robin for coverage)
                scenario = config.scenarios[(scenario_offset + run_id - 1) % total_scenarios]

                click.echo(f"\n  --- Run {run_id}/{num_runs} → {scenario.name} ---")

                # Clean previous output for this run
                if run_output.exists():
                    _rmtree(run_output)
                run_output.mkdir(parents=True, exist_ok=True)

                scenario_output = run_output / scenario.name
                scenario_output.mkdir(parents=True, exist_ok=True)

                click.echo(f"    {scenario.description}")

                prompt = render_generate_prompt(
                    config, cfg.name, project_root,
                    scenario=scenario, run_id=run_id,
                )
                try:
                    usage, trace = _run_copilot(
                        prompt, cfg, cwd=staging_dir, project_root=project_root,
                        run_output=run_output,
                        resolved=resolved,
                        generation_model=config.generation_model,
                    )
                    usage["config"] = cfg.name
                    usage["run_id"] = run_id
                    usage["scenario"] = scenario.name

                    # --- Session tracing (parsed from stdout, already on disk) ---
                    if trace is not None:
                        usage["session_id"] = trace.session_id
                        usage["model"] = trace.model
                        usage["copilot_version"] = trace.copilot_version
                        usage["loaded_resources"] = [
                            {
                                "resource_type": r.resource_type,
                                "name": r.name,
                                "plugin_name": r.plugin_name,
                                "content_length": r.content_length,
                                "timestamp": r.timestamp,
                            }
                            for r in trace.resources
                        ]
                        expected_skills = (
                            resolved.skill_names if resolved
                            else [ref.display_name for ref in cfg.skills]
                        )
                        expected_plugins = (
                            resolved.plugin_names if resolved
                            else [ref.display_name for ref in cfg.plugins]
                        )
                        # Build allowed directories (absolute) for path-based check
                        allowed_dirs = (
                            [str(p) for p in skill_paths]
                            + [str(p) for p in (resolved.plugin_paths if resolved else [])]
                        )
                        if not resolved:
                            allowed_dirs += [
                                str((project_root / ref.local_path).resolve())
                                for ref in cfg.plugins if ref.local_path
                            ]
                        comparison = compare_resources(
                            trace, expected_skills, expected_plugins,
                            allowed_dirs=allowed_dirs,
                        )
                        usage["resource_comparison"] = comparison

                        # Console summary
                        click.echo(
                            f"    📋 Session: {trace.session_id or '?'}"
                        )
                        if trace.resources:
                            names = ", ".join(trace.skill_names)
                            click.echo(
                                f"    📋 Skills loaded: {names} "
                                f"({len(trace.resources)})"
                            )
                        else:
                            click.echo("    📋 No skills loaded")

                        if comparison.get("contaminated"):
                            for c in comparison["contaminated"]:
                                click.echo(
                                    f"    🚨 CONTAMINATION: skill '{c['name']}' "
                                    f"loaded from outside this config: {c['path']}"
                                )
                        elif comparison.get("match"):
                            click.echo("    ✅ All skills/plugins match config")

                    # Extract and save the full chat log
                    events_file = run_output / "events.jsonl"
                    if events_file.exists():
                        chat_md = extract_chat_markdown(events_file)
                        if chat_md:
                            chat_path = run_output / "copilot-chat.md"
                            chat_path.write_text(chat_md, encoding="utf-8")

                    all_usage.append(usage)
                    click.echo(f"    ✅ {scenario.name} done")
                except RuntimeError as e:
                    click.echo(f"    ❌ {scenario.name} failed: {e.args[0]}")
                    # Capture partial usage even on failure (e.g., idle timeout
                    # after work completed)
                    partial_usage = e.args[1] if len(e.args) > 1 else None
                    if partial_usage and isinstance(partial_usage, dict):
                        partial_usage["config"] = cfg.name
                        partial_usage["run_id"] = run_id
                        partial_usage["scenario"] = scenario.name
                        partial_usage["failed"] = True
                        all_usage.append(partial_usage)
                    continue

        finally:
            # Restore the original skill_directories
            set_skill_directories(saved_skill_dirs)
            click.echo("  Restored global skill_directories")
            _cleanup_staging_dir(staging_dir, staging_links)
            click.echo(f"  Cleaned up staging directory")

        click.echo(f"  ✅ Done: {label}")

    click.echo(f"\n{'=' * 60}")
    click.echo(f"Generation complete: {len(configs_to_run)} configuration(s) × {num_runs} run(s)")
    click.echo(f"{'=' * 60}")

    # Write usage data and print summary
    if all_usage:
        import json
        usage_path = project_root / config.output.reports_directory / "generation-usage.json"
        usage_path.parent.mkdir(parents=True, exist_ok=True)
        usage_path.write_text(json.dumps(all_usage, indent=2), encoding="utf-8")
        click.echo(f"\n📊 Generation usage written to: {usage_path}")

        # Per-config summary
        click.echo(f"\n{'=' * 60}")
        click.echo("Generation Usage Summary")
        click.echo(f"{'=' * 60}")
        config_usage: dict[str, dict] = {}
        for u in all_usage:
            cn = u["config"]
            if cn not in config_usage:
                config_usage[cn] = {"input": 0, "output": 0, "calls": 0, "time": 0.0}
            config_usage[cn]["input"] += u.get("input_tokens", 0)
            config_usage[cn]["output"] += u.get("output_tokens", 0)
            config_usage[cn]["calls"] += u.get("api_calls", 0)
            config_usage[cn]["time"] += u.get("wall_time_seconds", 0)

        for cn, cu in config_usage.items():
            mins, secs = divmod(int(cu["time"]), 60)
            click.echo(
                f"  {cn:30s}: {cu['input']:>10,} in / {cu['output']:>8,} out / "
                f"{cu['calls']:>3} calls / {mins}m {secs}s"
            )
