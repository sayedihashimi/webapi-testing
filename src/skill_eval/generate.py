_GITIGNORE_CONTENT = "# Generated by skill-eval — do not commit generated output\n*\n"


def _ensure_gitignore(directory: Path) -> None:
    """Create a .gitignore in *directory* if one doesn't already exist."""
    gitignore = directory / ".gitignore"
    if not gitignore.exists():
        gitignore.write_text(_GITIGNORE_CONTENT, encoding="utf-8")


def _link_directory(target: Path, link: Path) -> None:
    """Create a directory junction (Windows) or symlink (other platforms).

    Windows junctions do not require elevated privileges.
    """
    if platform.system() == "Windows":
        subprocess.run(
            ["cmd", "/c", "mklink", "/J", str(link), str(target.resolve())],
            check=True,
            capture_output=True,
        )
    else:
        os.symlink(target.resolve(), link)


def _create_staging_dir(
    project_root: Path,
    cfg: Configuration,
    resolved: ResolvedConfiguration | None = None,
    include_directories: list[IncludeDirectory] | None = None,
) -> tuple[Path, list[Path]]:
    """Create an isolated working directory for a Copilot invocation.

    The staging directory contains directory links to ``prompts/`` and
    ``output/`` (so relative paths in the prompt still resolve correctly)
    and *only* the ``skills/`` and ``plugins/`` that the configuration
    declares.  This prevents Copilot from auto-discovering skills/plugins
    that belong to other configurations.

    When *include_directories* is provided, those directories are also
    linked into the staging directory so Copilot can access existing
    source code for analysis scenarios.

    When *resolved* is provided, uses the pre-resolved absolute paths.
    Otherwise falls back to resolving relative to project_root (legacy).

    Returns ``(staging_dir, created_links)`` so the caller can clean up.
    """
    staging = Path(tempfile.mkdtemp(prefix=f"skill-eval-{cfg.name}-"))
    links: list[Path] = []

    # Link prompts/ so @file references in the prompt resolve
    prompts_src = project_root / "prompts"
    if prompts_src.exists():
        prompts_link = staging / "prompts"
        _link_directory(prompts_src, prompts_link)
        links.append(prompts_link)

    # Link output/ so generated code lands in the real output tree
    output_src = project_root / "output"
    output_src.mkdir(parents=True, exist_ok=True)
    output_link = staging / "output"
    _link_directory(output_src, output_link)
    links.append(output_link)

    # Link user-specified include directories (for analysis scenarios)
    if include_directories:
        click.echo(f"  Linking {len(include_directories)} include director(ies):")
    for inc in (include_directories or []):
        inc_src = (project_root / inc.path).resolve()
        if not inc_src.exists():
            click.echo(f"    ⚠️  not found: {inc.path} (resolved to {inc_src})")
            continue
        link_name = inc.name or inc_src.name
        inc_link = staging / link_name
        if inc_link.exists():
            click.echo(f"    ⚠️  staging conflict: {link_name} already exists, skipping")
            continue
        _link_directory(inc_src, inc_link)
        links.append(inc_link)
        click.echo(f"    ✅ {link_name}/ → {inc_src}")

    # Determine skill/plugin paths (resolved or legacy)
    skill_paths = resolved.skill_paths if resolved else [
        (project_root / ref.local_path).resolve()
        for ref in cfg.skills if ref.local_path
    ]
    plugin_paths = resolved.plugin_paths if resolved else [
        (project_root / ref.local_path).resolve()
        for ref in cfg.plugins if ref.local_path
    ]

    # Only surface the skills declared by this configuration
    if skill_paths:
        skills_dir = staging / "skills"
        skills_dir.mkdir()
        for skill_src in skill_paths:
            link = skills_dir / skill_src.name
            _link_directory(skill_src, link)
            links.append(link)

    # Only surface the plugins declared by this configuration
    if plugin_paths:
        plugins_dir = staging / "plugins"
        plugins_dir.mkdir()
        for plugin_src in plugin_paths:
            link = plugins_dir / plugin_src.name
            _link_directory(plugin_src, link)
            links.append(link)

    return staging, links


def _rmtree(path: Path) -> None:
    """Remove a directory tree, handling locked files on Windows.

    Falls back to ``rd /s /q`` on Windows when ``shutil.rmtree`` fails
    (e.g. because dotnet build/run left DLLs locked).
    """
    try:
        shutil.rmtree(path)
    except PermissionError:
        if platform.system() == "Windows":
            subprocess.run(
                ["cmd", "/c", "rd", "/s", "/q", str(path)],
                check=False,
                capture_output=True,
            )
            if path.exists():
                raise
        else:
            raise


def _cleanup_staging_dir(staging: Path, links: list[Path]) -> None:
    """Remove the staging directory, safely removing junctions/symlinks first.

    ``os.rmdir`` removes junctions and symlinks without following them.
    """
    for link in links:
        try:
            os.rmdir(link)
        except OSError:
            pass
    shutil.rmtree(staging, ignore_errors=True)


